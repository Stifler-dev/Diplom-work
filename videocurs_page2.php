  <br><h2>Информация про Unity</h2>
  
  <br>Unity – это игровой движок, на котором строится большая часть современных игр на разных платформах. Посредством движка реально создавать игры, ориентированные на запуск на компьютерах (Windows, Linux, MacOS), мобильных телефонах, планшетных компьютерах (Android, iOS, Windows Phone) и даже игровых приставках PlayStation, Xbox, Nintendo.<br>


<br>Создание игр на движке Unity невозможно представить без написания дополнительного кода. В Unity поддерживается два языка программирования -<a href="">JavaScript </a> и <a href="">C#.</a>
Раньше также поддерживался язык Boo, но позже от него отказались.<br>

<br>Вы можете самостоятельно выбрать на каком языке программировать в Unity, так как особого значения это не имеет. Большинство разработчиков дают предпочтение языку <a href="">C#</a>
 так как он более мощный и найти различную документацию намного проще, нежели для языка JavaScript.<br>

<br>В Unity используется встроенные функции, которых нет ни в C#, ни в JavaScript. За счёт этого особо не имеет значения знает вы язык программирования или нет. Тем не менее, мы все же рекомендуем сперва пройти курс по изучению языка C#.<br>

