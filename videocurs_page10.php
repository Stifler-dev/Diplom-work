<br><h2>Информация про Qt</h2><br>


Qt (кьюти) - это специальный фреймворк, который позволяет создавать приложения с графическим дизайном, базами данных и мощным функционалом. Написание таких программ в основном осуществляется на <a href="https://ru.wikipedia.org/wiki/C%2B%2B">C++</a>, но также можно использовать другие языки программирование: <a href="https://ru.wikipedia.org/wiki/PHP">PHP</a>, <a href="https://ru.wikipedia.org/wiki/Java">Java</a>, <a href="https://ru.wikipedia.org/wiki/Python">Python</a>, <a href="https://ru.wikipedia.org/wiki/Ruby">Ruby</a> и другие.<br>

<br>В ходе данного видео курса мы будем создавать приложения используя <a href="https://ru.wikipedia.org/wiki/C%2B%2B">C++</a>, поэтому перед началом курса стоит ознакомиться с этим языком. <br>

<br>Платформа Qt C++ очень популярна, так как позволяет быстро и удобно создавать полноценное программное обеспечение, запуск и работа которого будет возможна практически на всех операционных системах: Mac, Windows, Linux и другие.<br>

<br>В Qt присутствует множество компонентов, которые позволяют писать код, создавать графический интерфейс, работать с дополнительными C++ библиотеками, подключаться к базе данных, а также компилировать и запускать приложения не выходя из самой программы.<br>

<br>Фреймворк Qt был использован во множестве популярных программ компаний, таких как: Skype, Телеграмм, проекты от Google, Panasonic Windows программа и многие другие.<br>

<br><h2>План курса</h2><br>

В ходе видео курса мы научимся работать с программой Qt, изучим различные компоненты этой программы, научимся создавать простые приложения, а также создадим полноценное приложение с графическим интерфейсом и функционалом.<br>

<br>К концу курса у вас будет достаточно знаний для разработки своих программ на основе платформы Qt.<br>

