 <br><center><h2>Язык Си для начинающих / #1 - Введение в Си</h2><br></center>

Представляем курс по изучению языка программирования Си. Си является одним из самых популярных языков в программировании и ща его изучение обычно берутся ещё в школе или университете. <br>

