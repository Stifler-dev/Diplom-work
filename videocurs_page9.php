<br><h2>Информация по языку Си</h2><br>

Сам по себе язык программирования Си многие считают устаревшим языком программирования. Несмотря на это, язык плотно и уже очень долго находиться в списке как один из наиболее популярных языков программирования в мире. И скажем сразу, язык из этого списка в ближайшее время никуда не уйдет.<br>

<br><div class="bimg1">
   <center> <img src="img/ci.png"></center>
  </div><br>

Сегодня язык Си используют не многие компании в качестве главного языка для создания какого-либо проекта. Это обусловлено тем, что существуют такие языки программирования, как:<a href="https://ru.wikipedia.org/wiki/C%2B%2B">C++</a>,<a href="https://ru.wikipedia.org/wiki/Java">Java</a>,<a href="https://ru.wikipedia.org/wiki/C_Sharp">C#</a> и тому подобные, которые выполнять роль нужного языка куда лучше чем <u>Си</u>.<br>

<br>Язык Си реально стар, ведь появился еще в 1972 году. Как понимаете, разрабатывался он с учётом компьютеров того времени, а они были как современный калькулятор по функционалу. Если бы не постоянная поддержка языка, то язык давным давно бы умер и все бы о нём забыли. За счёт того что он постоянно развивается, то даже сейчас он не утратил сил и на нём вполне можно разрабатывать. Язык низкоуровневый, а значит имеет хороший отклик и быстрое выполнение.<br>

<br><h2>Так почему же язык всё равно популярен?</h2><br>


Язык был и будет популярным, ведь на его основе на сегодняшний день построено огромное множество проектов, которые требуют постоянной поддержки и соответсвенно специалистов. Кроме того, язык является хорошим языком и написав программу на нём вы получаете достаточно быструю в плане выполнения программу. Ну и конечно же, язык Си для многих является словно родителем других языков и многие, включая университеты, предпочитают начинать путь изучения именно с этого языка. <br>

<br>то не глупо, ведь язык действительно повлиял на очень многие современные языки программирования и его принципы можно встретить практически во всех сегодняшних новомодных языках.<br>


<br><div class="bimg1">
   <center> <img src="img/ci1.png"></center>
  </div><br>


В общем, язык стоит изучить хотя бы по той причине, что он даёт базовое фундаментальное понимание принципов работы компьютера и языков программирования. В дальнейшем если вы захотите перейти на <a href="https://ru.wikipedia.org/wiki/C%2B%2B">C++</a>,<a href="https://ru.wikipedia.org/wiki/C_Sharp">C#</a>,<a href="https://ru.wikipedia.org/wiki/Java">Java</a> или даже
<a href="https://ru.wikipedia.org/wiki/Python">Python</a>,вы всё равно найдёте очень много схожего и изучение следующего языка вам явно будет даваться проще. Поэтому если вы еще не решились изучать или нет Си, но при этом ещё с программированием не сталкивались, то наш вам совет - учите Си и данный курс вам как раз предоставит всё необходимое чтобы его изучить.<br>

<br>Эти знания вам ни раз пригодятся, а главное как гордо можно будет заявить: «Я начинал изучение еще с языка Си», - красота.<br>

