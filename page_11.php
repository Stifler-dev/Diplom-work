       <br><h2><a href="">Java </a>13</h2>

       <br>Не так давно новые версии данного языка выходили раз в пятилетку. Но сейчас все по-другому. Oracle стал делать релизы каждые полгода и не собирается прекращать эту традицию. Подтверждением тому служат два выпуска 2019 года и актуальная на сегодняшний день 13-я версия.<br>

