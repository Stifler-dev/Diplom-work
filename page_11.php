       <br>По поводу частых релизов есть два мнения: одни считают, что это показатель современности и востребованности языка, тогда как другие ассоциируют их с нестабильностью. 
        И действительно, с одной стороны, разработчики больше доверяют надежным технологиям, так что уменьшенный срок поддержки последних версий играет не на пользу Java. С другой стороны, в современном мире невозможно быть конкурентоспособным без развития, внедрения новых решений и изменения укоренившихся традиций. <br>

        <br><h2>Raspberry PI 4</h2>

         <br>Сейчас программирование все больше внимания уделяет микроконтроллерам, носимым гаджетам и IoT. Где-то посередине между ними и стационарными ПК находится Raspberry PI – полноценный компьютер размером с ладонь. <br>

         <br>Уже первая его версия открыла много новых возможностей. Сейчас одноплатник обладает четырехъядерным процессором, usb-c и поддерживает 4k-мониторы. Raspberry PI 4, если сравнивать его с предыдущей версией линейки, сделал большой шаг вперед. А благодаря адекватной низкой стоимости его выбирают не только профессионалы, но и начинающие специалисты. <br>


