          <br>Сложно представить, чтобы корпоративный проект был написан на языке с нестрогой (слабой) типизацией. В наши дни программисты все чаще останавливают выбор на компилируемых языках, потому что им важнее надежность конечного продукта, а не быстрота разработки. <br>

          <br><h2><a href="">React </a>v16.8</h2>
          
          <br>Сейчас редко встречаются разработчики, не знающие и не умеющие применять эту библиотеку. Своей популярностью она обязана идеями, которые заложены в ее основу. Абсолютным лидером React стал благодаря компонентному подходу, изоморфизму, скорости, а также простоте в работе. <br>

