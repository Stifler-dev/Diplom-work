<br><h2>Где применяется Python?</h2><br>

Несколько основных функциональных достоинств Python:
<br>
<ul>
  <li>Умеет работать с расширениями xml/html;</li>
  <li>Поддерживает управление http-запросами;</li>
  <li>Обладает графическим интерфейсом;</li>
  <li>Помогает создавать веб-скрипты;</li>
  <li>Может работать с FTP;</li>
  <li>Способен оперировать картинками, видео и звуковыми файлами;</li>
  <li>Используется в разработке робототехники;</li>
  <li>Отвечает за создание научных, вычислительных площадок и ещё много всего другого.</li>
</ul><br>

Можно сказать, что Python отлично подходит для выполнения большей части ежедневных задач программистов. Участвует как в создании обычных бекапов или чтении писем с почты, так и в разработке крупных игр. Питон практически не имеет ограничений по сферам использования, что позволяет использовать его в проектах колоссальных масштабов. <br>

<br>Python часто применяется китами мира IT – Google и Яндекс. Если добавить к этому простоту создания программ, несложно понять, почему Питон заслуживает место в списке лучших языков.<br>

