   Еще через два года, в 2019 году, компания Google сообщила что разработка программ на Kotlin теперь является более предпочтительной, нежели разработка на Java.<br>

    <br>Большим плюсом языка Kotlin является его простота, а также легкая интеграция в другие Java проекты. Вы можете открыть любую Java программу, создать в ней новый файл что будет написан на Kotlin и у вас все будет друг с другом верно взаимодействовать и никаких ошибок выдаваться не будет.<br>

    <br><h2>А что же теперь с Java?</h2>

    <br>Если вы уже начали переживать насчет старого доброго языка Java, то могу вас сразу успокоить. Язык Java хоть и уже устарел как морально, так и физически, но он все равно никуда не исчезнет.<br>

    <br><u>Во-первых</u> язык по прежнему используется в сотнях тысяч различных программ и для их поддержки все равно будет использоваться язык Java.<br><br><br>
       <div class="bimg1">
   <img src="img/Kotlin1.png" alt="test" title="css3">
   </div><br>
    
    <u>Во-вторых</u>, язык Kotlin, как и многие другие новые технологии использует платформу Java Virtual Machine. Эта платформа написана на Java и чтобы такие языки как Kotlin и прочие могли работать, необходимо поддерживать саму платформу. Получается некий замкнутый круг, в котором Java очень уверенно чувствует себя.<br>

    <br>В таком случае может возникнуть новый вопрос, а зачем учить Kotlin, если Java все равно на этом ринге <u>«Босс»?</u> Все дело в том, что Kotlin это как глоток свежего воздуха для разработчиков Андроид программ. Писать все на Java сложно, ведь он громоздкий и зачастую неудобный. Kotlin же делает  разработку программ простой и с его применением вы теперь можете выполнять построение программ не задумываясь про сложные конструкции кода.<br>

   <br> <h2>План курса</h2><br>

    В курсе мы изучим язык Kotlin, начиная от простых моментов: переменных, циклов, массивов и так далее и заканчивая более сложными понятиями: классами, интерфейсами, перечислениями и многим другим.<br>

    <br>В конце курса мы также разработаем небольшую программу в Android Studio. Программа будет создана на основе языка Kotlin. Таким образом вы не только изучите сам язык программирования, но также научитесь применять его на практике для построения Андроид программ.<br>

