  <br>Существует несколько общепринятых стратегий для повышения доходности в сфере программирования. В статье мы расскажем про 3 действенных способа стать более богатым разработчиком.<br><br>

<h3>#1 - Разработка ПО и управленчиские качества</h3>

<br>Труд управленцев высоко оценивается в любой сфере, ИТ – не исключение. Технические и генеральные директора получают в среднем 200 тысяч рублей в месяц. Не отстают от них и архитекторы ПО с системными инженерами – их зарплата составляет около 180 тысяч рублей. <br>

<br>Как бы парадоксально это ни звучало, хороший директор вовсе не обязан обладать глубокими знаниями в технологиях. Главное ему нужно быть лидером: энергичным, организованным и харизматичным; уметь вести переговоры, работать с коллективом, координировать его деятельность. Именно такие личности чаще и быстрее всего добиваются успеха в управлении.<br>

<br>Что делать:<br>
<ul>
  <li>совершенствовать навыки руководителя (коммуникативные, тайм-менеджмент и т. д.);</li>
  <li>расширять круг общения, запускать проекты;</li>
  <li>говорить о своих достижениях внутри организации и снаружи;</li>
  <li>знать экономику проектов;</li>
  <li>всегда быть ответственным за результат ("запускать продукт", а не просто "писать код"). </li><br>
</ul>
<h2>#2 - Учить востребованные языки</h2>

<br>Согласно данным 2019 года, самые большие зарплаты получают специалисты <u>Elixir</u>,<a href="https://www.youtube.com/watch?time_continue=1&v=pfmxPtLIW34&feature=emb_logo">Go</a> ,<a href="https://www.youtube.com/watch?v=DjDL5cjLYwE&feature=emb_logo">Kotlin</a> ,Scala и Objective-C. Их средний ежемесячный доход равен 130 тысячам рублей.<br>

<br>Совсем немного отстают программисты <a href="https://www.youtube.com/watch?v=1MYbDz06B1E&feature=emb_logo">Ruby</a> ,<a href="https://www.youtube.com/watch?v=Zxpz5tRrUvU&feature=emb_logo">Java</a> ,<a href="https://www.youtube.com/watch?v=3FWqP80fNJM&feature=emb_logo">C#</a> ,<a href="https://www.youtube.com/watch?v=qSHP98i9mDU&feature=emb_logo">C++ </a> и <a href="https://www.youtube.com/watch?v=n0xtO0x81cg&feature=emb_logo">Python</a>  с окладом около 100 тысяч. Заметьте, что лучшие разработчики всех этих языков получают 250 или даже 300 тысяч рублей. <br>

<br>Что делать:<br>
<ul>
   <li>выучить Java — пользуется спросом, хорошо платят;</li>
   <li>выучить Golang — пользуется спросом, небольшая конкуренция, высокие зарплаты;</li>
   <li>выучить Python — удобно, перспективно, приличные оклады.</li>
</ul>



