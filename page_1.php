<h2>#3 - Освоить data science</h2>

<br>Сеть переполнена огромными массивами данных, и теперь крайне необходимы специалисты, которые умеют со всей этой информацией работать: обрабатывать, анализировать, структурировать и т.д. Таких людей совсем мало, и этот вид деятельности с уверенностью можно назвать профессией будущего.<br>

<br>Что делать:
<ul>
   <li>начать изучение data science, аналитики и языка программирования Python — вот 3 основные направления;</li>
   <li>принимать участие в конференциях, посещать встречи, посвященные данной теме. Сфера только зарождается, поэтому нужно внимательно следить за последними новостями.</li>
</ul>

<br><strong>Главный секрет</strong><br>

<br>Что бы ни говорила статистика, жизнь каждого конкретного человека редко соответствует стандартам и подчиняется правилам. Вы имеете все шансы как стать состоятельным разработчиком на Python, получая 350 тысяч рублей, так и очень востребованным инженером-программистом 1С с зарплатой больше 1 млн рублей, которые вам будут готовы платить за решение невероятно сложных задач. Во втором случае вы не попадете в средние статистические значения, зато станете удачным исключением.<br>

<br>Ключевые правила богатства:<br>
<br>
<ul>
  <li>хорошо владеть выбранным языком;</li>
   <li>больше беспокоиться не о самом программировании, а о том, как лучше решить задачи своих клиентов, заказчиков и работодателей; всего</li>
   <li>заводить новые знакомства, общаться, демонстрировать свои успехи, интересоваться работой других людей, кипеть среди разработчиков и заставлять их кипеть вместе с вами.</li>

<br>Одним словом, не ждать манну небесную – богатство само на вас не свалится. Хотя вы же программисты, вам и так это прекрасно известно.<br>

    

