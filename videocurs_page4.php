<br><h2>Информация про Swift</h2>

<br>Язык Swift был разработан компанией Apple в 2014 году. Считается очень молодым языком, но благодаря мощной поддержки со стороны Apple стал популярным практически с первого дня выхода. <br>

<br>Swift позволяет писать программный код для создания игр и приложений под всю продукцию компании Apple, а именно:<br>

<ul>
  <li>Планшеты - iPad;</li>
  <li>Телефоны - iPhone;</li>
  <li>Компьютеры - MacBook и iMac;</li>
  <li>Часы - Apple Watch;</li>
  <li>ТВ приставки - Apple TV.</li>
</ul><br>

<br><h2>Язык Objective C</h2>

<br>До появления языка все программы под Apple были написаны на основе языка Objective C. Со дня выхода языка Swift рейтинги языка Objective C сильно упали и на данный момент практически никто не изучает и не пишет новые приложения на основе этого языка.<br>

<br>Так происходит по той причине, что Apple сильно продвигает язык Swift, который объективно проще, удобнее и более функционален нежели его предшественник. <br>

<br>Изучать язык Objective C мы не рекомендуем, ведь никакой новой нужной для вас информации вы не подчеркнете при изучении этого устаревшего языка. <br>

<br><h2>План курса</h2>

<br>Изучение языка Swift не занимает много времени, так как компания Apple постаралась сделать его максимально простым и удобным в использовании. Он имеет простой и логичный синтаксис, который легко запомнить и несложно использовать.<br>

<br>В курсе мы отделим лишь один большой урок на изучение языка Swift. За урок вы изучите весь необходимый синтаксис языка и далее начнете разрабатывать приложения на его основе. <br>

<br>За курс вы создадите одно полноценное приложения с дизайном и функциональностью, а также разработаете небольшую игру с динамичностью, анимациями, звуками, переходами и прочим.<br>

