<br><h2>Распространение C++</h2><br>

В скоре после выхода язык С++ начал завоёвывать различные сферы программирования. Постепенно в него внедряли всё новые функции, что всё больше отдаляло его от роли дополнения к Си. Годами позже его перестали рассматривать продолжением или расширением и пришло время полностью отделиться от Си, тогда-то язык и назвали С++. С тех пор путь языков разошёлся, они стали независимыми (насколько это возможно).<br>

<br><div class="bimg1">
   <center> <img src="img/cpp.gif"></center>
