<br><h2>Информация про C++</h2><br>

С++ берёт основу в языке Си, активно разрабатываемом ещё в 1969-1973 годах. За разработку отвечала компания Bell Labs во главе с главным разработчиком Деннис Ритчи. Через 10 лет программист из Дании Бьёрн Страуструп, в своё время являвшийся разработчиком Си, выпускает расширенную версию языка под названием С++. Вначале своего пути C++ являлся всего лишь расширением, неким дополнением к Си, которое расширяло язык, добавляя в него принципы ООП. Сам Страуструп изначально рассматривал язык всего лишь как «Си с классами».<br>


<br>Дата рождения C++ приходится на 1983 год. Хоть он и не являлся полноценным языком, а лишь ответвлением от Си, но всё же имел ряд полезных функций за счёт которых и стал полноценным отдельным языком программирования. С выходом C++ в мире появилось ряд, ныне общераспространённых, понятий:<br>

<br><ul>
  <li>Инициация классов с объектами;</li><br>
  <li>Принцип наследования в ООП;</li><br>
  <li>Выгрузка и удаление функций из памяти;</li><br>
  <li>Создание виртуальных функций;</li><br>
  <li>Система исключений и многое другое.</li><br>
</ul>


<br><h2>А что же язык Си?</h2><br>

Язык Си по сей день сохраняет популярность даже несмотря на развитие C++ и доминирующее положение последнего языка в сфере функциональности. Почему старый и менее развитый язык даже сегодня популярен? Всё просто – переносить программы на новый язык - довольно затратное и долгое дело. Часть компаний просто плывут по течению и продолжают поддерживать свои программы на Си.<br>

<br>В сети очень многие специалисты дают советы, что начать изучение С-подобных языков лучше с Си. Они говорят, что только так можно лучше понять суть С++. В реальности – это неправильное мнение. Если начать изучение с С++, в арсенале разработчика появятся знания о более новых и современных возможностях языка. Если когда-то появится необходимость перейти на Си, это не потребует много времени и сил. Даже сам факт необходимости перехода на довольно старый Си уже вызывает большие сомнения.<br>

<br>Но если вы все же хотите изучить Си перед языком C++, то предлагаем вам ознакомиться с большим видео уроком, в котором вы сможете познакомиться с этим языком:<br>

