Сегодня С++ - это функциональный и мощный язык, который получил в наследство от Си массу ключевых возможностей по управлению памятью. По этой причине он активно применяется в системном программировании.<br>

<br>Несколько ключевых сфер использования:<br>

<br><ul>
  <li>Разработка операционных систем. в том числе система Windows преимущественно базируется на С++;</li><br>
  <li>Создание драйверов;</li><br>
  <li>Написание программ с нуля;</li><br>
  <li>Разработка и поддержка антивирусных приложений и прочего.</li><br>
</ul><br>

С++ ушёл далеко за пределы исключительно системного программирования. Он может участвовать в создании приложений всех уровней, в которых стоит акцент на быстродействии и работе под высокими нагрузками. Применяется для формирования графических редакторов и прикладных приложений.<br>


<br>Весьма много игровых движков, предназначенных для визуализации огромного игрового мира, основываются на С++. За последние года всё больше мобильных приложений используют данный язык. Даже в сфере веба нашлось место этому языку. Всевозможные веб-приложения активно используют С++, не обязательно в качестве основы, может просто для реализации вспомогательных функций, но факт остаётся фактом. Если подытожить: сложнее найти сферы, в которых С++ не может применяться, чем там, где используется этот язык.<br>


