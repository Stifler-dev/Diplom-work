<br><h2>Ключевые особенности C++</h2><br>


С++ относится к компилируемым языкам, то есть компилятор изменяет код C++, преображая его в машинный с помощью предустановленных инструкций. Все платформы отличаются некоторыми особенностями, из-за чего нельзя взять и перенести программу с одной на другую платформу. Точнее, перенести можно, но она там не запустится. При этом исходный код приложения преимущественно переносится. Единственное исключение – использование специфических возможностей конкретной ОС.<br>

<br>У С++ есть компиляторы и инструменты для работы с кодом на большинстве известных платформ. Это позволяет скомпилировать исходники под любую платформу.<br>

<br>С++ поддерживает написание программ в стиле ООП, чего не умеет Си. В этом случае приложение становится лишь набором классов, объектов, которые способны взаимодействовать с другими элементами кода. Это помогает в разработке крупных приложений.<br>


<br><h2>Как поэтапно развивался С++?</h2><br>

Шаги развития С++:<br>


<br><ul>
  <li>1979-1980 – С++ появился и позиционировался, как язык Си с дополнительными классами;</li><br>
  <li>1983 – язык начал называться, как все мы привыкли С++;</li><br>
  <li>1985 – релиз коммерческого варианта С++. В этот же год появилась первая бумажная книга «Языка программирования C++», она включала описание функций, возможностей и правил использования языка;</li><br>
  <li>1989 – выпущена вторая версия языка с расширенными возможностями;</li><br>
   <li>С 1990 до 2011 – медленное развитие и добавление функционала. Также стоит упомянуть, что в 1998 году впервые попытались стандартизировать язык. Тот стандарт впоследствии назвали ISO/IEC 14882:1998, сегодня - С++98. В 2003 году появилась следующий вариант стандарта C++03;</li><br>
  <li>2011 – издание стандарта C++11, в нём появилось действительно много нововведений. Обновление обогатило язык обилием полезных возможностей;</li><br>
  <li>2014 – выпуск относительно малого добавления к предыдущей версии стандарта. Его прозвали C++14;</li><br>
  <li>2017 – последний крупный релиз языка.</li><br>
</ul><br>


