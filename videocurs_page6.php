 <br><center><h2>Изучение PHP | Урок №1 - Основы PHP</h2><br></center>

PHP – это один из самых распространённых языков сценариев с широкой сферой применения и opensource-кодом. Если упростить определение, это язык программирования, использующийся для создания веб-приложений (сценариев) и исполняющийся на стороне сервера. <br>


