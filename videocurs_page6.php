  </div><br>

PHP один из тех языков, с которых можно начинать путь программиста. Несмотря на лёгкость освоения, язык очень функционален, он готов выполнять даже сложные задачи. Даже если вы не знаете ничего о PHP и программировании в целом, особого труда с освоением возникнуть не должно. Нет сомнений, что уже спустя пару часов изучения PHP вы научитесь создавать первые скрипты.<br>

<br>PHP относится к тем языкам, что регулярно улучшаются и развиваются. Он точно будет занимать лидирующие места в сфере веба ещё достаточно долго. Ближайшие 10-15 лет программисты на PHP будут востребованы.<br>

<br><h2>Что значит «серверный язык»?</h2><br>

Язык PHP ценят за мощность и гибкость. Он выступает связующим звеном между клиентом и базой данных, а также выполняет базовую обработку всех входных параметров от пользователя. Всё это стало возможным благодаря работе скриптов на стороне сервера.<br>


<br><u>Практическая сторона вопроса:</u><br>

<br><ul>
  <li>Удобно создает страницы или их фрагменты, которые одинаково отображаются у каждого пользователя;</li><br>
  <li>PHP все равно какая производительность компьютера у пользователя или какая операционная система стоит. Вся обработка данных происходит на сервере;</li><br>
  <li>Для получения новых данных на странице обязательно потребуется её обновление (если не прибегать к JS). До обновления страницы стандартными средствами PHP нельзя что-либо менять на ней. Иначе говоря, язык не интерактивный. С помощью Ajax, уже реально обновлять данные веб-страницы без её перезагрузки.</li>
</ul><br>


