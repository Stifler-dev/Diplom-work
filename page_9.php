        <br><h2>3.<a href="">Yii2 </a></h2><br>

        Высокая скорость загрузки и усиленная безопасность – вот чем известен Yii2. В этом инструменте также заложен принцип Don't repeat yourself, т.е. неповторения кода. А с помощью Ajax и JQuery фреймворк облегчает создание высокомасштабируемых веб-приложений. Достоинствами Yii2, помимо прочего, являются:<br>

        <ul>
          <li>Улучшенная безопасность. Yii2 способен защищать сайты и приложения от разного рода атак, в том числе подделки cookie, внедрения SQL-кода, CSRF и межсайтового скриптинга.</li>

          <li>Ускоренная разработка. Фреймворк генерирует базовые функции CRUD, поэтому программисты работают быстрее, что не только экономит время, но и уменьшает стоимость разработки.</li>

          <li>Простая установка. Когда каждая минута на счету, Yii радует пользователей шаблоном инсталляции. В результате установка и настройка продукта происходят в разы быстрее.</li>
        </ul><br>
       
        
