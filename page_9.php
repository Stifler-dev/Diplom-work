        <br><h2>4.<a href="">Zend Framework</a></h2><br>

          Этот продукт создан по методологии agile и ориентирован на разработку корпоративных веб-приложений. Zend позволяет внедрять в проект любые специфичные фичи. Пятилетняя поддержка дает возможность делать стабильные бизнес-программы с длительным жизненным циклом. <br>

          <br>Кроме безопасности и великолепной производительности Zend обладает такими преимуществами:<br>
          <ul>
            <li>Набор компонентов аутентификации. Процесс аутентификации усложняет PHP-разработку. Решением этой проблемы во фреймворке выступает коллекция инструментов для тестирования форм. Таким образом, этот функционал не приходится разрабатывать с нуля.</li>

            <li>Zend Studio. Это проприетарная интегрированная среда, в которой разработчики могут писать код намного быстрее. Отладка становится более удобной благодаря интеграции Xdebug, Z-Ray и Zend Debugger. Также есть возможность делать развертку на разных серверах, в частности поддерживается  Microsoft Azure и Amazon Web Services.</li>

            <li>Улучшенная документация. В руководстве по фреймворку описано более 500 примеров на одной тысяче страниц. Там вы точно сможете найти подходящие решения для своих проектов. В качестве бонуса Zendcasts содержит множество обучающих роликов, посвященных функционалу этого инструмента.</li>
          </ul><br>
         
