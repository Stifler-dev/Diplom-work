        <br><h2>2.<a href="">Laravel</a></h2><br>

        Laravel изначально проектировался под создание многофункциональных веб-приложений. Он может похвастать отличным движком шаблонов, который выполняет множество обычных задач. Что касается функционала, нужно отметить Restful Routing, аутентификацию и кэширование. Благодаря этим опциям Laravel делает разработку более быстрой. Но есть и другие причины, по которым мы советуем применять этот фреймворк:<br>

        <ul>
          <li>Высокий уровень безопасности. Мы уже акцентировали внимание на том, что для PHP-фреймворка безопасность является очень важной характеристикой. В данном случае вы будете работать в безрисковой среде, потому как Laravel блокирует все вредоносные активности и обладает прекрасной защитой от случайных и скрытых внедрений SQL-кода. </li>

          <li>
            Встроенные почтовые сервисы. За счет интегрированного почтового сервиса разработанное вами веб-приложение сможет отправлять пользователям SMS и Slack уведомления. Фреймворк имеет простой в использовании API, а также драйвера для Mailgun, Amazon SES, SMTP и Mandrill.
          </li>

          <li>Обширный функционал. Разработчики могут внедрять разные виды готового функционала благодаря пакетам, плагинам, компонентам и модулям, что позволяет существенно сократить время разработки.</li>

          <li>Улучшенная аутентификация. Фреймворк позволяет пользователям без особых усилий создавать логику аутентификации. А это, как известно, далеко не самая простая задача.</li>
        </ul><br>

