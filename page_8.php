      <br>Каждый год появляются новые языки программирования, которым предвещают мировое господство в сфере программирования. Что же изменилось за этот год?<br>

      <br>Перед любым программистом стоит нелегкая задача: среди огромного множества существующих языков выбрать для себя наиболее подходящий. Некоторые из них только недавно стали популярными, в то время как другие резко пошли на спад.<br>

      <br>В статье мы расскажем про 7 языков, которые будут востребованы в 2020 году, но стоит помнить, что язык необходимо выбирать не из-за рейтинга и популярности, а исходя из ваших проектов и целей. К примеру, будет глупо выбирать язык Python, если ваша основная сфера это разработка под iOS.<br>

      <br><h2>1.<a href="">Python</a></h2><br>
   
       Кто точно не собирается сбавлять обороты, так это Python. По данным Stack Overflow, за последние 5 лет только этот язык программирования может похвастаться стабильным ростом популярности.<br>

       <br>Индекс <a href="https://www.techrepublic.com/article/10-most-popular-programming-languages-why-python-will-replace-c-and-java/">TIOBE прогнозирует</a>, что совсем скоро Python обгонит Java и C. Так что, выбрав его своим главным инструментом, вы точно не будете одиноки<br>

       <br>Python достаточно прост в изучении, поэтому писать код на нем можно научиться легко и быстро. Он относится к языкам общего назначения, а значит, подходит для создания любых продуктов. Нельзя не обратить внимания и на открытый исходный код, а также наличие огромного количества библиотек. <br>

       <br>Искусственный интеллект, нейросети, машинное обучение, большие данные, робототехника и кибербезопасность – Python уверенно захватывает самые актуальные разделы информационных технологий. Популярность языка подтверждает и тот факт, что он чаще других появляется на вводных университетских курсах. <br>

       <br><h2>Преимущества:</h2><br>

       <ul>
         <li>прозрачность, точная структура, краткий синтаксис;</li><br>
          <li>работает на платформах ОС Linux и Microsoft Windows, MS-DOS, macOS, UNIX и остальных Unix-like системах;</li><br>
           <li>отлично совместим с Unix, элементами аппаратного обеспечения и сторонним ПО с громадной библиотекой;</li><br>
            <li>высокоскоростная обработка, благодаря которой Python прекрасно справляется с написанием не только маленьких сценариев, но и огромных программ, вроде Blender 3D.</li><br>
       </ul>

       
     
       <h2>Недостатки:</h2><br>

        <ul>
          <li>отсутствие модификаторов доступа классов, определяющих уровень их закрытости.</li>
        </ul><br>

        <h2>2.<a href="">Java</a></h2>
 
          <br>Был создан в 1995-ом, быстро завоевал популярность и остается известнейшим языком по сегодняшний день. Но, как мы уже говорили выше, скоро Python может обойти Java по числу разработчиков. <br>

          <br>Сфера применения данного языка программирования общего назначения – приложения и игры для смартфонов, особенно на базе Android. <br>

          <br>Java является одним из самых высокооплачиваемых языков. Он используется на 7 млрд устройств. А число его разработчиков достигло 9 млн человек по всему миру.<br>

          <br>Это важнейшая платформа, на которой пишется почти все коммерческое ПО. В ноябре текущего года она возглавила ТОП-50 лучших ЯП по мнению <a href="https://www.tiobe.com/tiobe-index/">сообщества TIOBE</a>.<br> 
         
         <div class="bimg1">
          <br><img src="img/tobie.png" alt="old" title="old"> 
        </div>
    


        <br><h2>Преимущества:</h2><br>

       <ul>
         <li>межплатформенность;</li><br>
          <li>классные фреймворки для корпоративной разработки (стеки Java EE, Spring).</li><br>
           <li>относительно низкий порог вхождения новичков;</li><br>
            <li>существование Java Reflection API, благодаря которой были созданы очень мощные либы, например, Hibernate, Jackson и прочие;</li><br>
            <li>возможность писать для Android;</li><br>
            <li>интеграция с остальными JVM-языками: Groovy, Kotlin и Scala. </li><br>
       </ul>



        <h2>Недостатки:</h2><br>

        <ul>
          <li>не такая высокая производительность, как у нативных сред;</li><br>
            <li>большое количество бойлерплейта;</li><br>
              <li>фреймворки массивные и непростые в конфигурации (взять хотя бы Spring, для которого даже разработали Spring Boot – фактически, получается один фреймворк над другим).</li><br>
        </ul><br>




      <h2>3.<a href="">JavaScript</a></h2>

      <br>Пользователей JS день ото дня становится все больше. В данный момент он лидирует по популярности, о чем говорят свежие результаты <a href="https://insights.stackoverflow.com/survey/2019">опроса программистов Stack Overflow. </a> опроса программистов Stack Overflow.<br>

      <br>JavaScript – это ОО-язык, решающий такие задачи как создание интерактивных и динамических страниц, анимации и масштабирования. Сообщество JavaScript – одно из самых крупных. Язык активно поддерживается и имеет много фреймворков, библиотек.<br>

       <div class="bimg1">
          <br><img src="img/3.png" alt="old" title="old"> 
        </div>
    
     <br>На GitHub он первый по числу pull request. Огромное множество современных сайтов сейчас применяют именно JavaScript.<br>

     <br>Запуская этот язык в браузере, вы не нуждаетесь в загрузке дополнительного ПО. Кроме того, JS прекрасно подходит для изучения новичками. За счет простоты и быстродействию, JavaScript стал любимым инструментом многих стартапов и крупных технологических фирм.<br>


     <br><h2>Преимущества:</h2><br>

       <ul>
         <li>простота в изучении (в нем немного низкоуровневых концепций);</li><br>
          <li>громадное сообщество, масса материалов и обучающих курсов в открытом доступе;</li><br>
           <li>быстрое развитие, совершенствование, благодаря чему язык становится все более удобным, большое количество таких надстроек как Typescript;</li><br>
            <li>запуск во всех ОС с поддерживающим его браузером.</li><br>
       </ul>


      
        <h2>Недостатки:</h2><br>

        <ul>
          <li>чересчур много свободы, из-за чего при написании кода можно легко наделать ошибок (нет строгих правил, проверочных тестов при компиляции и т.п.);  </li><br>
            <li>нет возможности скрыть код, низкий уровень безопасности.</li><br>
        </ul><br>


        <h2>4.<a href="">C++</a></h2>

        <br>Этот легендарный язык часто становится базовым, и только овладев им разработчики начинают осваивать другие. C++ обладает более чем 30-летней историей, и даже несмотря на возраст остается очень популярным в геймдеве.<br>

        <br>Многие всем известные приложения написаны именно на C++. К ним относятся Google Chrome, LinkedIn, Photoshop, PayPal, PDFReader и прочие. Microsoft также продолжает применять его для создания своих продуктов. Если вы профессионально программируете на C++, у вас есть неплохие шансы получить престижное рабочее место с соответствующей зарплатой в Google и Facebook.<br>

        <br>Сейчас C++ продолжает развиваться сообществом. Выпускается много обновлений, например, C++14, C++17 или еще ожидаемый C++20.<br>


        <br><h2>Преимущества:</h2><br>

       <ul>
         <li>поддержка сообществом, множество разработчиков начинают изучать IT-науки именно с C или C++;</li><br>
          <li>применяется в масштабных проектах, в частности, high-load;</li><br>
           <li>тщательный контроль памяти, можно увеличить производительность и защиту;</li><br>
            <li>от С унаследовал гибкость, т.е. совместимость с разными аппаратными устройствами.</li><br>
       </ul>

       
          <h2>Недостатки:</h2><br>

        <ul>
          <li>много трудных синтаксических конструкций, порой не только для понимания, но и банально для прочтения; </li><br>
            <li>для повышения производительности C++ не самый оптимальный вариант, потому что есть Python с множеством сторонних модулей, благодаря которым разработка становится намного более простой и быстрой.</li><br>
        </ul><br>



        <h2>5.<a href="">C#</a></h2>

           <br>Зная Java, выучить C# сравнительно легко. Этот ОО-язык появился в 2001 году и с тех пор демонстрирует только положительную динамику: его популярность и поддержка сообществом стабильно возрастают. <br>


           <br>C# – продукт корпорации Microsoft, работающий на платформе .NET. Данную технологию применяют при создании мобильных приложений, настольных, а также игр для Windows. Несмотря на то, что язык опирается на базу C++ и C, он гораздо более прост в использовании. 

Синтаксис C# похож на Java. На этом языке программирования написан код платформы Xamarin, с помощью которой разрабатывают приложения для различных ОС: iOS, Android и прочих. Обычно без C# не обходится VR и 2D-, 3D-видеоигры.<br>

          <br><h2>Преимущества:</h2><br>

       <ul>
         <li>легкость в изучении;</li><br>
          <li>огромное сообщество, в интернете есть множество бесплатных материалов и обучающих курсов;</li><br>
           <li>возможность создавать кроссплатформенное ПО и приложения для Андроид и устройств Apple.</li><br>
       </ul>
      


     <br> <h2>6.<a href="">Swift</a></h2>


     <br>Как и Python, вышедший в 2014-ом году Swift является ярким примером быстрорастущего сектора в сфере программирования. Он считается наиболее подходящим для разработки программ и приложений для Apple. Поэтому, если вы хотите создавать продукты под Mac OS и iO, обязательно начинайте знакомиться со Swift.<br>

     <br>Весьма юный возраст не помешал языку зарекомендовать себя практичным и функциональным, тем более, если сравнивать с предыдущими аналогами. К тому же он не требует больших объемов памяти и легок в поддержке.<br>



     <br> <h2>7.<a href="">PHP</a></h2>

     <br>Язык общего назначения PHP уступает по популярности JS и Python, но все равно остается очень востребованным. Он используется в основном для разработки на серверной стороне.

PHP применяют такие гиганты как Facebook, мессенджер Slack и почтовая платформа MailChimp. На этом языке написана известная система WordPress. PHP тоже легко выучить, к тому же он обладает несколькими очень полезными функциями, например, образцовой отладкой и широкой экосистемой.<br>


    <br><h2>Итог</h2>

    <br>Вот вы и узнали, какие языки программирования будут самыми актуальными в 2020 году. Поэтому готовьтесь: упоминания об этой семерке в ближайшем будущем порядком намозолят вам глаза. <br>

    <br>В плане выбора языка, учитывайте, что каждый из них предназначен для решения определенных задач, а значит, обладает своими преимуществами и слабыми сторонами. К примеру, если вы хотите заниматься созданием мобильных приложений, выбирайте путь Python-разработчика. Для работы с интерактивными элементами на веб-страницах вам понадобится знание JavaScript. А прекрасным вариантом для программирования игр будет C#. <br>

    <br>Но что бы вы ни выбрали, знайте, с помощью любого языка из этого списка можно написать отличный код для самых разных проектов. Дерзайте!<br>






