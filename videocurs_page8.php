<br><h2>Информация про C#</h2><br>

Благодаря незаурядной мощности языка, на него пал выбор разработчиков движка <a href="">Unity</a>.Сегодня является одним из топовых движков для игр на Windows. Выпуск и активное использование движка пошли на руку C#, который стал ещё популярнее.<br>

<br>После базового изучения принципов работы с языком и его синтаксисом, можно сразу перейти к углублённому изучению библиотек и смежных технологий. Большинство современных проектов пишутся с их привлечением, на чистом C# работают редко.<br>

<br>Если сравнить С# с прочими языками, его можно назвать довольно молодым, хотя за плечами уже пройден немалый путь. Впервые полноценная версия языка появилось после выхода Microsoft Visual Studio .NET, событие датируется февралём 2002 года. На сегодня актуальная версия - C# 7.0, выпущенная 7.03.2017 параллельно с Visual Studio 2017.<br>

<br><div class="bimg1">
   <center> <img src="img/win.gif"></center>
  </div><br>

В <a href="">C#</a> используется синтаксис, сильно напоминающий Си и приближённый к <a href="https://ru.wikipedia.org/wiki/C%2B%2B">C++</a>, <a href="https://ru.wikipedia.org/wiki/Java">Java</a>. Если есть познания в одном из перечисленных языков, изучить C# станет значительно легче. <br>

<br>C# это объектно-ориентированный язык, который немало позаимствовал из С++ и Java. Для примера, С# обладает поддержкой наследственности, полиморфизма, повторную загрузку операторов, статической типизации. Такой подходит помогает в решении распространённых задач в сфере разработки крупных приложений, сохраняющих гибкость, расширяемость и масштабируемость. C# всё ещё развивается, после каждого дополнения в языке появляются новые функции, преимущественно довольно полезные. Среди них: асинхронная работа, переменное связывание, лямбды и т. п.<br>

<br><h2>Какова роль .NET?</h2><br>

Если речь заходит о C#, часто подразумеваются технологии, связанные с платформой .NET (WPF, ASP.NET). То же самое справедливо в обратном направлении, говоря про .NET, думают про C#. Безусловно, понятия имеют жёсткую взаимосвязь, но не являются синонимами. C# - это язык, созданный для взаимодействия с фреймворком .NET. Тем не менее .NET – это довольно широкое понятие.<br>


<br><div class="bimg1">
   <center> <img src="img/win2.gif"></center>
  </div><br>

Сейчас вспоминаются слова Билла Гейтса, не дословно, но смысл таков:<u>"Платформа .NET – лучшее творение Microsoft"</u>.Может так и есть, ведь фреймворк очень мощный и постоянно используется в разработке приложений.<br>

<br>Ключевые черты платформы:<br>

<br><ul>
  <li>Работает параллельно с разными языками. Популярностью С# во многом обязан общеязыковой среде CLR. Сейчас платформа способна работать с С#, VB.NET, C++, F#, но и на этом список не заканчивается, ведь она работает с диалектами, что привязаны к .NET (наподобие Delphi.NET). После компиляции кода с любого из перечисленных языков, вся интерпретируется в общий язык CIL – это своеобразный ассемблер для .NET. Такой подход позволяет использовать несколько языков для создания подключаемых модулей программы;</li><br>
  <li>Кроссплатформенность. Данную платформу реально переносить, хоть и есть отдельные ограничения. Сегодня актуальная версия фреймворка работает на всех поддерживаемых Виндовс: от Windows Vista до «десятки». За счёт проекта Mono появилась возможность разрабатывать программы под Linux (различные дистрибутивы), Android и iOS;</li><br>
  <li>Обширная библиотека классов. .NET Framework обладает единой, унифицированной библиотекой классов, с которой работают все поддерживаемые языки. Библиотека классов пригодится при создании любых программ: от блокнота до огромного веб-сайта;</li><br>
  <li>Масса встроенных технологий. Среда CLR в сочетании с библиотекой классов – это основа для большого пакета вспомогательных технологий. Их могут использовать все программисты во время разработки приложений. В качестве примера, при взаимодействии с базами данных можно использовать технологию ADO.NET. Во время создания графических редакторов с многочисленными функциями удобно использовать WPF. Во время веб-разработки наверняка используют ASP.NET.</li><br>
</ul>

